void
print_comment(schemaDefinition *def)
{
	char kbuf[256];
	char vbuf[256];

	snprintf(kbuf, sizeof(kbuf), KEY_PREFIX "%s%s",
		 def->prefix, def->key);
	snprintf(vbuf, sizeof(vbuf), "\"%s\"",
		 def->value ? def->value : def->key);

	if (def->type)
	    printf(" *   %-50s %-30s %s\n",
		   kbuf, vbuf, def->type);
	else
	    printf(" *   %-50s %s\n",
		   kbuf, vbuf);

	return;
}

void
print_define(schemaDefinition *def)
{
	printf("%s\t%s\n", def->prefix, def->key);
	return;
}

